(function (window) {
  window.FsQrUploadModules = window.FsQrUploadModules || {};
  var modules = window.FsQrUploadModules;

  function createIdModeController(options) {
    var idInput = options.idInput;
    var clearFormError = options.clearFormError;

    function generateRandomId() {
      var chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
      var result = '';
      for (var i = 0; i < 6; i += 1) {
        result += chars.charAt(Math.floor(Math.random() * chars.length));
      }
      return result;
    }

    function applyGeneratedId(idValue) {
      var autoGeneratedIdInput = document.getElementById('autoGeneratedId');
      var hiddenIdInput = document.getElementById('hiddenId');
      if (autoGeneratedIdInput) {
        autoGeneratedIdInput.value = idValue;
      }
      if (hiddenIdInput) {
        hiddenIdInput.value = idValue;
      }
    }

    function setupInitialAutoId() {
      applyGeneratedId(generateRandomId());
    }

    function bindIdModeToggle() {
      var radios = document.querySelectorAll('input[name="idMode"]');
      radios.forEach(function (radio) {
        radio.addEventListener('change', function () {
          var manualGroup = document.getElementById('manualIdGroup');
          var autoGroup = document.getElementById('autoIdGroup');
          var nameInput = document.getElementById('name');
          var hiddenIdInput = document.getElementById('hiddenId');
          clearFormError();

          if (this.value === 'manual') {
            manualGroup.style.display = 'block';
            autoGroup.style.display = 'none';
            nameInput.required = true;
            hiddenIdInput.name = '';
            nameInput.name = 'name';
          } else {
            manualGroup.style.display = 'none';
            autoGroup.style.display = 'block';
            nameInput.required = false;
            nameInput.name = '';
            hiddenIdInput.name = 'name';
            applyGeneratedId(generateRandomId());
          }
        });
      });
    }

    function bindIdValidation() {
      if (!idInput) {
        return;
      }
      idInput.addEventListener('input', function () {
        var value = this.value;
        var isValid = /^[a-zA-Z0-9]{0,6}$/.test(value);

        if (!isValid) {
          this.setCustomValidity('半角英数字のみ使用してください');
          this.style.borderColor = '#ef4444';
        } else if (value.length < 6 && value.length > 0) {
          this.setCustomValidity('6文字すべて入力してください');
          this.style.borderColor = '#f59e0b';
        } else {
          this.setCustomValidity('');
          this.style.borderColor = value.length === 6 ? '#10b981' : '#e5e7eb';
        }
      });
    }

    function getCurrentId() {
      var selectedMode = document.querySelector('input[name="idMode"]:checked');
      var isAutoMode = selectedMode && selectedMode.value === 'auto';
      if (isAutoMode) {
        var hiddenId = document.getElementById('hiddenId');
        return hiddenId ? hiddenId.value : '';
      }
      return idInput ? idInput.value.trim() : '';
    }

    return {
      setupInitialAutoId: setupInitialAutoId,
      bindIdModeToggle: bindIdModeToggle,
      bindIdValidation: bindIdValidation,
      getCurrentId: getCurrentId
    };
  }

  modules.idMode = {
    createIdModeController: createIdModeController
  };
})(window);
