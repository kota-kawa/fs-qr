def load_shards(language: str) -> dict[str, dict[str, str]]:
    return {
        shard: dict(values)
        for shard, values in load_locale_section_shards(
            LOCALES_DIR, language, "phrases"
        ).items()
    }


def save_shards(language: str, shards: dict[str, dict[str, str]]) -> None:
    for shard, values in shards.items():
        save_locale_section_shard(LOCALES_DIR, language, "phrases", shard, values)


def quality_target_shard(key: str) -> str:
    return article_phrase_shard_for_key(key) or "quality_fill"


def add_quality_fill(language: str, additions: dict[str, str]) -> None:
    shards = load_shards(language)
    changed = False
    for key, value in additions.items():
        target_shard = quality_target_shard(key)
        target = dict(shards.get(target_shard, {}))
        if target.get(key) != value:
            target[key] = value
            shards[target_shard] = target
            changed = True
        for shard, values in shards.items():
            if shard != target_shard and key in values:
                values.pop(key, None)
                changed = True
    if changed:
        save_shards(language, shards)


def fill_legal_terms() -> None:
    for language in SUPPORTED_LANGUAGES:
        if language in (DEFAULT_LANGUAGE, "en"):
            continue
        additions = {}
        for key, translations in LEGAL_TERMS.items():
            value = translations.get(language)
            if value:
                additions[key] = value
        add_quality_fill(language, additions)


def fill_common_article_terms() -> None:
    for language, values in COMMON_ARTICLE_TRANSLATIONS.items():
        if len(values) != len(COMMON_ARTICLE_KEYS):
            raise ValueError(
                f"{language} has {len(values)} common article translations; "
                f"expected {len(COMMON_ARTICLE_KEYS)}"
            )
        add_quality_fill(language, dict(zip(COMMON_ARTICLE_KEYS, values, strict=True)))


def fill_remaining_long_terms() -> None:
    for language, values in REMAINING_LONG_TRANSLATIONS.items():
        if len(values) != len(REMAINING_LONG_KEYS):
            raise ValueError(
                f"{language} has {len(values)} remaining translations; "
                f"expected {len(REMAINING_LONG_KEYS)}"
            )
        add_quality_fill(language, dict(zip(REMAINING_LONG_KEYS, values, strict=True)))


def fix_mixed_values() -> None:
    for language, additions in MIXED_VALUE_FIXES.items():
        add_quality_fill(language, additions)


def normalize_remaining_extras() -> None:
    base_keys = list(load_locale_section(LOCALES_DIR, "en", "phrases"))
    for language in SUPPORTED_LANGUAGES:
        if language in (DEFAULT_LANGUAGE, "en"):
            continue
        shards = load_shards(language)
        current = load_locale_section(LOCALES_DIR, language, "phrases")
        extras = sorted(set(current) - set(base_keys))
        if not extras:
            continue

        for extra in extras:
            best = max(
                base_keys, key=lambda key: SequenceMatcher(None, extra, key).ratio()
            )
            score = SequenceMatcher(None, extra, best).ratio()
            extra_value = current[extra]
            if score >= 0.85 and best not in current:
                shards.setdefault(quality_target_shard(best), {})[best] = extra_value
            for values in shards.values():
                values.pop(extra, None)
        save_shards(language, shards)


def main() -> None:
    fill_legal_terms()
    fill_common_article_terms()
    fill_remaining_long_terms()
    fix_mixed_values()
    normalize_remaining_extras()


if __name__ == "__main__":
    main()
